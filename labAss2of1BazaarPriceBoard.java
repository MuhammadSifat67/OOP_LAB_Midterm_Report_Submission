public class labAss2of1BazaarPriceBoard 
{

    public static void main(String[] args) 
    {
        int num = 39;
/*1. Write a Java program to print the multiplication table of a given number using a for loop.
[TheBazaarPriceBoard]*/   
        System.out.println("Quick Price Board: ");
        
        for (int i=1; i<= 10; i++)
        {
            System.out.println(num+ "*" +i+ "="+ (num*i));
        }
    }    
}
